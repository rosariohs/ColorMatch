    //algoritmo de identificacion
    public String identifyColorPixel(int pixel, HashMap<Integer, String> colorHashMap,
                                     boolean complementary)
    {
        // nombre del mejor color aproximado, a principio null
        String colorName = null;
        Integer colorRGB = null;
        // la mayor diferencia es 255 para cada componente de color
        int currentDifference = 3 * 255;

        // se obtiene la cantidad de R G B del pixel
        int pixelColorR = Color.red(pixel);
        int pixelColorG = Color.green(pixel);
        int pixelColorB = Color.blue(pixel);

        if (complementary)
        {
            //R G B del complementario, se actualizan los campos
            pixelColorR = 255 - pixelColorR;
            pixelColorG = 255 - pixelColorG;
            pixelColorB = 255 - pixelColorB;
        }

        //se itera por el map
        for (Map.Entry<Integer, String> colorItem : colorHashMap.entrySet())
        {
            String colorNameAux = colorItem.getValue();
            Integer colorRGBAux = colorItem.getKey();

            int colorR = Color.red(colorRGBAux);
            int colorG = Color.green(colorRGBAux);
            int colorB = Color.blue(colorRGBAux);

            //se calcula la diferencia entre el color del hashmap y el color captado
            int difference = Math.abs(pixelColorR - colorR) + Math.abs(pixelColorG - colorG) + Math.abs(pixelColorB - colorB);

            //se guarda el que la diferencia sea menor
            if (currentDifference > difference) {
                currentDifference = difference;
                colorName = colorNameAux;
                colorRGB = Color.rgb(colorR, colorG, colorB);

                if(complementary)
                {
                    colorSetting.colorRGBComplementary = colorRGB;
                }
            }
        }

        return colorName;
    }
    //aproxima el pixel central a los colores en el hashmap
    public void processColor(Integer pixel) {
        colorSetting.colorName = identifyColorPixel(pixel, colorHashMapGeneral, false);
    }


    //aproxima el pixel central a los colores en el hashmap
    public void processComplementaryColor(Integer pixel) {
        colorSetting.colorNameComplementary = identifyColorPixel(pixel, colorHashMapGeneral, true);
    }

    //sugerencia de gamas
    public void processSuggestion(Integer pixel) {
        colorSetting.colorNameSuggestion = identifyColorPixel(pixel, colorHashMapBasic, false);
    }

